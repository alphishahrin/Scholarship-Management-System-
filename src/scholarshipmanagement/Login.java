    private void loginbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginbtnActionPerformed
        // TODO add your handling code here:
        String selectedlogin = loginas.getSelectedItem().toString();
        
        if(selectedlogin == "Student")
        {
            String username = usertxt.getText();
            String password = new String(passwordtxt.getPassword());
            
            ConnectMSSQL obj  = new ConnectMSSQL();
            String temp = obj.checkStuLogin(username,password);
            
            if(temp.length()==0)
            {
                JOptionPane.showMessageDialog(rootPane, "Invalid Username or Password");
            }
            else
            {
                new StudentDashboard(temp).setVisible(true);
                this.setVisible(false);
            }
        }
        else
        {
            String username = usertxt.getText();
            String password = new String(passwordtxt.getPassword());
            
            ConnectMSSQL obj  = new ConnectMSSQL();
            String temp = obj.checkProfLogin(username,password);
            
            if(temp.length()==0)
            {
                JOptionPane.showMessageDialog(rootPane, "Invalid Username or Password");
            }
            else
            {
                new ProfessorDashboard(temp).setVisible(true);
                this.setVisible(false);
            }
        }
    }//GEN-LAST:event_loginbtnActionPerformed

