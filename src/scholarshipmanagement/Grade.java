    private void gradeKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_gradeKeyTyped
        
        char c = evt.getKeyChar();
        if (!((c >= '0') && (c <= '9') ||
            (c == KeyEvent.VK_BACK_SPACE) ||
            (c == '.')||
            (c == KeyEvent.VK_DELETE))) {
        getToolkit().beep();
        evt.consume();
        }
    }//GEN-LAST:event_gradeKeyTyped

