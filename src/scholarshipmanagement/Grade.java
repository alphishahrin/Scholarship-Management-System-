    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       if(grade.getText().length()==0)
        {
            JOptionPane.showMessageDialog(this, "Grade is Empty","Error",JOptionPane.ERROR_MESSAGE);
        }
        else if(year.getText().length()==0)
        {
            JOptionPane.showMessageDialog(this, "Year is Empty","Error",JOptionPane.ERROR_MESSAGE);
        }
        else if(semester.getText().length()==0)
        {
            JOptionPane.showMessageDialog(this, "Semester is Empty","Error",JOptionPane.ERROR_MESSAGE);
        }
        else if(courseName.getText().length()==0)
        {
            JOptionPane.showMessageDialog(this, "Course Name is Empty","Error",JOptionPane.ERROR_MESSAGE);
        }

        else
        {
            CourseResultInfo user = new CourseResultInfo();

            user.setCourseName(courseName.getText().toString());
            user.setGrade(Float.valueOf(grade.getText().toString()));
            user.setSemester(Integer.valueOf(semester.getText().toString()));
            user.setYear(Integer.valueOf(year.getText().toString()));
            user.setStudentID(Integer.valueOf(StudentId.toString()));

            try
            {
                new ConnectMSSQL().addCourseResultInfo(user);

                JOptionPane.showMessageDialog(this, "Updated Successfully","Updated",JOptionPane.INFORMATION_MESSAGE);
                this.setVisible(false);
                new Grade(StudentId).setVisible(true);
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(this, "Error Occured","Error",JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

